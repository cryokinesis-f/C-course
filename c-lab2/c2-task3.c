/*
Вводится неотрицательное целое число. Перевести его в массив цифр (начальные эле-
менты массива должны содержать старшие цифры числа) и вывести «красиво» раз-
ложение числа: без нулевых слагаемых, без показателя у 10 в слагаемом десятков и без
сомножителя 10 в слагаемом единиц:
305471 = 3*10ˆ5 + 5*10ˆ3 + 4*10ˆ2 + 7*10 + 1,
305470 = 3*10ˆ5 + 5*10ˆ3 + 4*10ˆ2 + 7*10.
*/


//
#include <stdio.h>
#include <math.h>


void print_decomposition(int num) {

    // Обработка нуля
    if (num == 0) {
        printf("0\n");
        return;
    }
    
    // Массив для хранения цифр числа
    int digits[10] = {0};
    int len = 0;       // Длина числа (количество цифр)
    int temp = num;    // Временная переменная для обработки числа
    
    // Разбиваем число на цифры и сохраняем их в массив
    while (temp > 0) {
        digits[len] = temp % 10;  // Сохраняем последнюю цифру
        len++;
        temp /= 10;                // Убираем последнюю цифру из числа
    }
    
    int first = 1;  // Флаг для отслеживания первого выводимого слагаемого
    
    // Проходим от конца массива к началу массива
    for (int i = len - 1; i >= 0; --i) {
        
        if (digits[i] == 0) continue;
        
        // Выводим знак "+" перед всеми слагаемыми, кроме первого
        if (!first) printf(" + ");
        first = 0;  // После первого вывода сбрасываем флаг
        
        if (i == 0) {
            // Для единиц выводим просто цифру
            printf("%d", digits[i]);
        } else if (i == 1) {
            // Для 10^1 выводим без степени
            printf("%d*10", digits[i]);
        } else {
            // Для остальных выводим с показателем степени
            printf("%d*10^%d", digits[i], i);
        }
    }
    printf("\n");  // Переход на новую строку после вывода
}

int main() {
    int num;
    printf("Enter a non-negative integer: ");
    scanf("%d", &num);
    
    print_decomposition(num);
    
    return 0;
}